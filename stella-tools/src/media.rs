#[async_trait]
impl Tool for GenerateVideo {
    fn schema(&self) -> ToolSchema {
        ToolSchema {
            name: "generate_video".into(),
            description: "Submit an asynchronous text-to-video job with the configured BYOK \
                          video provider; returns the job id to check with poll_video. Video \
                          costs real money and requires approval from the host runtime before \
                          provider submission."
                .into(),
            input_schema: serde_json::json!({
                "type": "object",
                "properties": {
                    "prompt": {
                        "type": "string",
                        "description": "What to generate"
                    },
                    "duration_secs": {
                        "type": "integer",
                        "description": "Target duration in seconds, at least 1 (default 5); \
                                        drives the cost estimate"
                    },
                    "label": {
                        "type": "string",
                        "description": "Short human label for the artifact filename"
                    }
                },
                "required": ["prompt"]
            }),
            read_only: false,
        }
    }

    async fn execute(&self, input: &Value, root: &std::path::Path) -> ToolOutput {
        let Some(prompt) = input.get("prompt").and_then(|v| v.as_str()) else {
            return ToolOutput::Error {
                message: "`prompt` is required".into(),
            };
        };
        let duration_secs = match input.get("duration_secs") {
            None => DEFAULT_VIDEO_DURATION_SECS,
            Some(v) => match v.as_u64().and_then(|secs| u32::try_from(secs).ok()) {
                Some(secs) if secs >= 1 => secs,
                _ => {
                    return ToolOutput::Error {
                        message: "`duration_secs` must be a positive integer".into(),
                    };
                }
            },
        };
        let mut request = VideoRequest::new(prompt, duration_secs);
        if let Some(label) = input.get("label").and_then(|v| v.as_str()) {
            request.label = label.to_string();
        }

        let operation_id = operation_key(
            self.operation_ids.as_ref(),
            MediaKind::Video,
            self.provider.id(),
        );
        request.operation_id = Some(operation_id.clone());
        let jobs = open_jobs(root);
        match jobs.claim_operation(&operation_id, MediaKind::Video, self.provider.id()) {
            Ok(None) => {}
            Ok(Some(MediaOperationState::VideoSubmitted { provider_job_id })) => {
                return match jobs.get(&provider_job_id) {
                    Ok(Some(job)) => video_submitted(&job),
                    Ok(None) => {
                        reconciliation_required(&operation_id, "persisted video handle is missing")
                    }
                    Err(error) => reconciliation_required(&operation_id, error),
                };
            }
            Ok(Some(MediaOperationState::VideoCompleted {
                provider_job_id,
                artifact_path,
            })) => {
                return ToolOutput::Ok {
                    content: format!(
                        "video job `{provider_job_id}` already completed → {artifact_path}"
                    ),
                };
            }
            Ok(Some(state)) => {
                return reconciliation_required(&operation_id, format!("state {state:?}"));
            }
            Err(error) => {
                return ToolOutput::Error {
                    message: format!("media_operation_journal_unavailable: {error}"),
                };
            }
        }
        let estimate = self.provider.capabilities().estimate_video(duration_secs);
        let spend_request = MediaSpendRequest::new(
            operation_id.clone(),
            MediaKind::Video,
            self.provider.id(),
            estimate.as_ref().map(|estimate| estimate.estimated_usd),
            estimate.map_or_else(
                || format!("{duration_secs}s video (cost unavailable)"),
                |estimate| estimate.detail,
            ),
        );
        if self.spend_gate.authorize(&spend_request).await != CostDecision::Approve {
            if let Err(error) = jobs.cancel_operation(&operation_id) {
                return reconciliation_required(&operation_id, error);
            }
            return spend_denied(&spend_request);
        }

        let job = match self.provider.generate_video(request).await {
            Ok(job) => job,
            Err(error) => return reconciliation_required(&operation_id, error),
        };
        // Persist the handle BEFORE reporting success — a paid job whose id
        // exists only in this reply would be orphaned by a dropped session.
        if let Err(error) = jobs.complete_video_operation(&operation_id, &job) {
            return reconciliation_required(&operation_id, error);
        }
        video_submitted(&job)
    }
}

pub struct PollVideo(pub Arc<dyn MediaProvider>);

#[async_trait]
impl Tool for PollVideo {
    fn schema(&self) -> ToolSchema {
        ToolSchema {
            name: "poll_video".into(),
            description: "Check a video job submitted with generate_video, reconciling its \
                          state live against the provider. On success the video is saved \
                          under .stella/artifacts/ and the file path is returned; while \
                          queued/running, call again after a short wait."
                .into(),
            input_schema: serde_json::json!({
                "type": "object",
                "properties": {
                    "job_id": {
                        "type": "string",
                        "description": "The provider job id returned by generate_video"
                    }
                },
                "required": ["job_id"]
            }),
            read_only: false,
        }
    }

    async fn execute(&self, input: &Value, root: &std::path::Path) -> ToolOutput {
        let Some(job_id) = input.get("job_id").and_then(|v| v.as_str()) else {
            return ToolOutput::Error {
                message: "`job_id` is required".into(),
            };
        };
        let jobs = open_jobs(root);
        // The persisted handle supplies the identity assigned at submit
        // (artifact id, label); the state below never comes from it.
        let job = match jobs.get(job_id) {
            Ok(Some(job)) => job,
            Ok(None) => {
                return ToolOutput::Error {
                    message: format!(
                        "no persisted video job `{job_id}` — was it submitted with \
                         generate_video?"
                    ),
                };
            }
            Err(e) => {
                return ToolOutput::Error {
                    message: format!("video job store unavailable: {e}"),
                };
            }
        };
        // Live reconciliation (L-V3): report the provider's answer, never a
        // cached state.
        let status = match stella_media::resume(&jobs, self.0.as_ref(), job_id).await {
            Ok(status) => status,
            Err(e) => {
                return ToolOutput::Error {
                    message: format!("video poll failed: {e}"),
                };
            }
        };
        match status.state {
            MediaJobState::Succeeded => {
                let Some(artifact) = status.artifact else {
                    return ToolOutput::Error {
                        message: format!(
                            "video job `{job_id}` succeeded but the provider returned no \
                             artifact"
                        ),
                    };
                };
                let store = match open_store(root) {
                    Ok(s) => s,
                    Err(e) => return e,
                };
                // Persist under the id assigned at submit, so events and the
                // final file share one identity across a resume.
                match store.save_with_id(
                    &job.artifact_id,
                    &artifact.bytes,
                    artifact.kind,
                    &artifact.extension,
                    &artifact.label,
                ) {
                    Ok(saved) => {
                        if let Err(error) = jobs.complete_video_job(job_id, &saved.path) {
                            return reconciliation_required(job_id, error);
                        }
                        ToolOutput::Ok {
                            content: format!(
                                "video \"{}\" → {} (model {}, ${:.4})",
                                saved.label, saved.path, artifact.model, artifact.cost_usd
                            ),
                        }
                    }
                    Err(e) => ToolOutput::Error {
                        message: format!("could not persist the video: {e}"),
                    },
                }
            }
            MediaJobState::Failed { reason } => {
                let _ = jobs.remove(job_id);
                ToolOutput::Error {
                    message: format!("video job `{job_id}` failed: {reason}"),
                }
            }
            MediaJobState::Queued | MediaJobState::Running => {
                let state = match status.state {
                    MediaJobState::Queued => "queued",
                    _ => "running",
                };
                let progress = status
                    .progress
                    .map(|p| format!(" ({:.0}% reported)", p * 100.0))
                    .unwrap_or_default();
                ToolOutput::Ok {
                    content: format!(
                        "video job `{job_id}` is {state}{progress} — poll again after a \
                         short wait"
                    ),
                }
            }
        }
    }
}

pub struct GenerateSvg;

#[async_trait]
impl Tool for GenerateSvg {
    fn schema(&self) -> ToolSchema {
        ToolSchema {
            name: "generate_svg".into(),
            description: format!(
                "Validate, sanitize, and save an SVG document you authored to \
                 .stella/artifacts/, returning the file path. The SVG is treated as \
                 untrusted code: scripts, event handlers, and external references are \
                 stripped. If it fails to parse, the error names the offending line and \
                 column — fix the SVG and retry (up to {DEFAULT_SVG_ATTEMPTS} attempts \
                 total)."
            ),
            input_schema: serde_json::json!({
                "type": "object",
                "properties": {
                    "svg": {
                        "type": "string",
                        "description": "The complete SVG document (<svg …>…</svg>)"
                    },
                    "label": {
                        "type": "string",
                        "description": "Short human label for the artifact filename"
                    }
                },
                "required": ["svg"]
            }),
            read_only: false,
        }
    }

    async fn execute(&self, input: &Value, root: &std::path::Path) -> ToolOutput {
        let Some(svg) = input.get("svg").and_then(|v| v.as_str()) else {
            return ToolOutput::Error {
                message: "`svg` is required".into(),
            };
        };
        // The bounded repair loop maps onto the agent
        // loop here: a failed attempt returns the parse error for the model
        // to fix, instead of driving an in-process `SvgGenerator` (that port
        // stays for model-backed CLI glue).
        let processed = match SvgPipeline::process(svg) {
            Ok(p) => p,
            Err(e) => {
                return ToolOutput::Error {
                    message: format!("invalid SVG: {e}"),
                };
            }
        };
        let label = input.get("label").and_then(|v| v.as_str()).unwrap_or("svg");
        let store = match open_store(root) {
            Ok(s) => s,
            Err(e) => return e,
        };
        match store.save(processed.svg.as_bytes(), MediaKind::Svg, label) {
            Ok(saved) => {
                let sanitized = if processed.removed.is_empty() {
                    String::new()
                } else {
                    format!(" — sanitized: stripped {}", processed.removed.join(", "))
                };
                ToolOutput::Ok {
                    content: format!("svg \"{}\" → {}{sanitized}", saved.label, saved.path),
                }
            }
            Err(e) => ToolOutput::Error {
                message: format!("could not persist the SVG: {e}"),
            },
        }
    }
}

